// Helper function to get all meals
export const getAllMeals = () => {
  return [
    ...healthyMealDatabase.morningDrinks,
    ...healthyMealDatabase.breakfast,
    ...healthyMealDatabase.lunch,
    ...healthyMealDatabase.eveningSnacks,
    ...healthyMealDatabase.dinner,
    ...healthyMealDatabase.nightDrinks
  ];
};

// Helper function to get meals by category
export const getMealsByCategory = (category) => {
  const categoryMap = {
    morningDrink: 'morningDrinks',
    breakfast: 'breakfast',
    lunch: 'lunch',
    eveningSnack: 'eveningSnacks',
    dinner: 'dinner',
    nightDrink: 'nightDrinks'
  };

  return healthyMealDatabase[categoryMap[category]] || [];
};

// Helper function to get meal by ID
export const getMealById = (mealId) => {
  const allMeals = getAllMeals();
  return allMeals.find(meal => meal.id === mealId);
};

// Helper function to calculate daily calories
export const calculateDailyCalories = (mealSelections) => {
  let totalCalories = 0;
  Object.values(mealSelections).forEach(mealId => {
    const meal = getMealById(mealId);
    if (meal) totalCalories += meal.calories;
  });
  return totalCalories;
};

// Helper function to get meals by tags
export const getMealsByTag = (tag) => {
  const allMeals = getAllMeals();
  return allMeals.filter(meal => meal.tags && meal.tags.includes(tag));
};
